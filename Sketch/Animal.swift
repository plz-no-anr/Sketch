import Foundation

struct Animal{
    var name: String
    var image: String
    var group: String
    var isFly: Bool
    
    init(name:String, image:String, group:String, isFly:Bool){
        self.name = name
        self.image = image
        self.group = group
        self.isFly = isFly
    }
    
}
